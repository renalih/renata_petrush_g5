package tasks.homework.week1.cycletask;

public class CycleFor {


    public void processFor(){

        System.out.println("Нечетные числа:");

        for (int i=3; i <= 19; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
            }

        }
        System.out.println('\n');
    }



}

